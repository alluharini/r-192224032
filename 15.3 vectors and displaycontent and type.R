# Create vectors
numeric_vector <- c(1, 2, 3, 4, 5)
character_vector <- c("apple", "blueberry", "banana")
logical_vector <- c(TRUE, FALSE, TRUE, FALSE, TRUE)

# Display content and type of vectors
print("Numeric Vector:")
print(numeric_vector)
print(paste("Type:", typeof(numeric_vector)))

print("Character Vector:")
print(character_vector)
print(paste("Type:", typeof(character_vector)))

print("Logical Vector:")
print(logical_vector)
print(paste("Type:", typeof(logical_vector)))
